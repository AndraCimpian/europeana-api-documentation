<h2>Data</h2>
<p>Creates an untyped name/value pair. The name can have two versions: name and displayName. The name attribute is used to identify the data pair within the KML file. The displayName element is used when a properly formatted name, with spaces and HTML formatting.<br/><span class="required">nb: this call is not yet meet the spec, needs work, and may change at any time.</span></p>

