<h2>query-syntax</h2>

	<p>Europeana users internally the <a href="http://lucene.apache.org/solr/" target="_blank">Apache SOLR</a> platform to store its data and therefore it supports the <a href="https://lucene.apache.org/core/4_1_0/queryparser/org/apache/lucene/queryparser/classic/package-summary.html#package_description" target="_blank">Apache Lucene Query Syntax</a> for its queries. Advanced users are encouraged to use Lucene and Apache SOLR guides to get most out of the Europeana repository. For others we supply in what follows a basic guide for querying Europeana.</p>

